#include <iostream>
using namespace std;

int main() {

    int s;
    int n;
    cout << "Enter first number: ";
    cin >> s;
    cout << "Enter second number: ";
    cin >> n;

    int sum = s + n;
    int diff = s - n;
    int prod = s * n;

    cout << "Sum = " << sum << endl;
    cout << "Difference = " << diff << endl;
    cout << "Product = " << prod << endl;

    
}