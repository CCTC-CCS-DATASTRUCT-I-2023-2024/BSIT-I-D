#include <iostream>

using namespace std;

int main(){

    int a = 10; 
    int b = 50;
    int c = 70;

    int sum = a + b;
    int sub = a - c;
    int mul = a * b;
    int div = a / c;

    cout << "Sum = " << sum << endl;
    cout << "Sub = " << sub << endl;
    cout << "Mul = " << mul << endl;
    cout << "Div = " << div << endl;
}