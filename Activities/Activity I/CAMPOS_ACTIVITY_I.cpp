#include <iostream>
using namespace std;

int main (){
    int a = 10; 
    int b = 20;
    int c = 30;

    int sum = a+b+c;
    int diff = a-b-c;   
    int prod = a*b*c;
    int quo = a/b/c;

    cout<<"Sum = "<<sum<<endl;
    cout<<"Difference is : "<<diff<<endl;
    cout<<"Product is : "<<prod<<endl;
    cout<<"Quotient is : "<<quo<<endl;
}