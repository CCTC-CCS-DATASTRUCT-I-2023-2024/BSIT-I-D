#include <iostream>
using namespace std;

int main() {
    int a = 50, b = 50, c = 43;
    int sum = a + b + c;
    int diff = a - b - c;
    int prod = a * b * c;
    int quo = a / c / b;
    
    cout << "The sum is " << sum << endl;
    cout << "The difference is " << diff << endl;
    cout << "The product is " << prod << endl;
    cout << "The quotient is " << quo << endl;
    
}
