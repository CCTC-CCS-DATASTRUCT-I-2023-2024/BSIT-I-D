#include <iostream>
using namespace std;

int main()
{
    int x = 20;
    int y = 30;
    int z = 40;

    int Product = x * y * z;
    int Sum = x + y + z;
    int Difference = x - y - z;
    int Qoutient = x / y / z;

    cout << "Product is: " << Product << endl;
    cout << "Sum is: " << Sum << endl;
    cout << "Difference is: " << Difference << endl;
    cout << "Qoutient is: " << Qoutient << endl;
}
